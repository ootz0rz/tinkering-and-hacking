# https://leetcode.com/problems/same-tree/?envType=study-plan-v2&envId=top-interview-150

# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def isSymmetric(self, root: Optional[TreeNode]) -> bool:
        if root is None:
            return True
        
        def explore(l, r):
            if not l or not r:
                return not l and not r
            
            if l.val != r.val:
                return False
            
            return explore(l.right, r.left) and explore(l.left, r.right)
            

        return explore(root.left, root.right)


